        //
        try {
            $lm = new location_m;
            $lm->name = $request->name;
            if ($lm->save()){

                return back();
            }
        } catch (\Exception  $th) {
            if ($th->getCode() == '230001'){

                return back()
                    ->with('error', 'location already exists');
            }

            return back()
                    ->with('error', 'location already exists');
        }


        return $request;
    }

